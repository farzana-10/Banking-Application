    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            // TODO add your handling code here:
            String facc=txtfacc.getText();
            String bal=txtbal.getText();
            String toacc=txttoacc.getText();
            String amount=txtamount.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost/loyalbank","root","");
            con1.setAutoCommit(false);
            
            
            //sender
            pst1=con1.prepareStatement("update account set balance=balance- ? where acc_id=?");
            pst1.setString(1,amount);
            pst1.setString(2,facc);
            pst1.executeUpdate();
            
            //receiver
            pst2=con1.prepareStatement("update account set balance=balance+ ? where acc_id=?");
            pst2.setString(1,amount);
            pst2.setString(2,toacc);
            pst2.executeUpdate();
            
            pst3=con1.prepareStatement("insert into transfer(f_account,to_account,amount) values(?,?,?)");
            pst3.setString(1,facc);
            pst3.setString(2,toacc);
            pst3.setString(3,amount);
            pst3.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Amount Transfered");
            con1.commit();
            
            
            
            
            
        } 
        
        catch (ClassNotFoundException ex) {
            Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex);
            try {
                con1.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Transfer.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
            
            
            
            
           
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

