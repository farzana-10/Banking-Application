            // TODO add your handling code here:
            
            String cust_id=txtfacc.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost/loyalbank","root","");
            pst1=con1.prepareStatement("select c.cust_id,c.firstname,c.lastname,balance from customer c,account a where c.cust_id=a.cust_id and acc_id=?");
            pst1.setString(1, cust_id);
            rs=pst1.executeQuery();
            
            
            if(rs.next()==false){
                JOptionPane.showMessageDialog(this, "Account Id Not Found");
            }
